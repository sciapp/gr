void gks_lookup_afm(int font, int chr, stroke_data_t *s)
{
  int psfont;

  psfont = abs(font) - 1;
  if (chr < 0)
    chr += 256;
  if (chr == '-')
    chr = '+';

  if (psfont >= 100 && psfont <= 130)
    psfont -= 100;
  else if (psfont >= 0 && psfont <= 31)
    psfont = psmap[psfont] - 1;
  else
    psfont = 8;

  s->left = 0;
  s->right = widths[psfont][chr % 256];
  s->size = caps[psfont];
  s->bottom = descenders[psfont];
  s->base = 0;
  s->cap = s->size;
  s->top = s->cap + 120;
}
